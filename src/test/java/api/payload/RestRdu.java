package api.payload;

public class RestRdu {

	String rduHost;
	String username;
	String password;
	String rduPort;
	
	public String getRduHost() {
		return rduHost;
	}
	public void setRduHost(String rduHost) {
		this.rduHost = rduHost;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getRduPort() {
		return rduPort;
	}
	public void setRduPort(String rduPort) {
		this.rduPort = rduPort;
	}

	
	
	

}
